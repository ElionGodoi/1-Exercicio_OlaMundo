package cursoUdemy;

public class Aula_42_OperadoresAritiméticos{
	public static void main (String[] args) {
		//Operadores Aritméticos
		
		int a= 21;
		int b= 5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		double f= 21;
		double g= 5;
		
		System.out.println(f+g);
		System.out.println(f-g);
		System.out.println(f*g);
		System.out.println(f/g);
		System.out.println(f%g);
		
		Double p = 3.0;
		System.out.println(Math.pow(p, 9));
		
		
	}
}
